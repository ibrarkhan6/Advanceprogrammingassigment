# At first we need to import math.
import math

# The  Ceil of the a
a = 2.3
myceil_result = math.ceil(a)
print(f"The ceil of {a} is: {myceil_result}")

# The  floor of the a
myfloor_result = math.floor(a)
print(f"The floor of {a} is: {myfloor_result}")

#  The factorial of  a
a = 5
myfactorial_result = math.factorial(a)
print(f"The factorial of {a} is: {myfactorial_result}")

#  The square root of the a
a = 16
mysqrt_result = math.sqrt(a)
print(f"The square root of {a} is: {mysqrt_result}")

# The value of the  23
myexpression_result = eval("2 + 3 * 4 - 1")
print(f"The value of the expression 2 + 3 * 4 - 1 is: {myexpression_result}")
